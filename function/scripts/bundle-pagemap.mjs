// Build a chunk_id -> { url, title } map so chat citations can link to the
// specific documentation page instead of the source root.
//
// Inputs (all committed, so this runs offline):
//   .opencrane/llmstxt/<source>/llms.txt       — per-page titles + URLs (GitBook index)
//   .opencrane/llmstxt/<source>/llms-full.txt  — page bodies (H1 = page title)
//   .opencrane/chunks.json                     — chunk_id + content per source
//
// A chunk is matched to a page by finding which single page body contains one
// of its longer content lines. Unmatched chunks simply fall back to the source
// root URL at runtime.
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { dirname, resolve, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "../..");
const llmstxtDir = join(root, ".opencrane/llmstxt");
const out = resolve(here, "../src/pagemap.generated.ts");

function parseLlmsTxt(text) {
  // "- [Title](https://.../page.md): description" -> { Title: humanUrl }
  const map = new Map();
  for (const m of text.matchAll(/^- \[(.+?)\]\((\S+?)\)/gm)) {
    map.set(m[1].trim(), m[2].replace(/\.md$/, ""));
  }
  return map;
}

function splitPages(fullText) {
  // llms-full.txt concatenates pages; each starts with an H1 line ("# Title")
  const pages = [];
  let current = null;
  for (const line of fullText.split("\n")) {
    if (/^# \S/.test(line)) {
      if (current) pages.push(current);
      current = { title: line.slice(2).trim(), body: "" };
    } else if (current) {
      current.body += line + "\n";
    }
  }
  if (current) pages.push(current);
  return pages;
}

// pages per source, each with a resolved URL (when llms.txt knows the title)
const sources = {};
for (const name of readdirSync(llmstxtDir)) {
  const idx = join(llmstxtDir, name, "llms.txt");
  const full = join(llmstxtDir, name, "llms-full.txt");
  if (!existsSync(idx) || !existsSync(full)) continue;
  const titleToUrl = parseLlmsTxt(readFileSync(idx, "utf8"));
  const pages = splitPages(readFileSync(full, "utf8"))
    .map((p) => ({ ...p, url: titleToUrl.get(p.title) }))
    .filter((p) => p.url);
  sources[name] = pages;
}

const chunks = JSON.parse(readFileSync(join(root, ".opencrane/chunks.json"), "utf8"));
const pageMap = {};
let matched = 0;
for (const chunk of chunks) {
  const pages = sources[chunk.source_name];
  if (!pages) continue;
  // candidate needles: longest content lines first, specific enough to be unique
  const needles = chunk.content
    .split("\n")
    .map((l) => l.trim())
    .filter((l) => l.length >= 20)
    .sort((a, b) => b.length - a.length)
    .slice(0, 5);
  for (const needle of needles) {
    const hits = pages.filter((p) => p.body.includes(needle) || p.title === needle.replace(/^#+\s*/, ""));
    if (hits.length === 1) {
      pageMap[chunk.chunk_id] = { url: hits[0].url, title: hits[0].title };
      matched++;
      break;
    }
  }
}

writeFileSync(
  out,
  `// GENERATED by scripts/bundle-pagemap.mjs from .opencrane/{chunks.json,llmstxt} — do not edit.\n` +
    `import type { PageMap } from "./mcp.js";\n` +
    `export const PAGE_MAP: PageMap = ${JSON.stringify(pageMap)};\n`
);
console.log(`Bundled page map -> ${out} (${matched}/${chunks.length} chunks mapped to specific pages)`);
